    private void ver(){
        
         
         
         if(!"".equals(txtTransferencia.getText())){
             transferencia = Double.parseDouble(txtTransferencia.getText());
             
         }else{
             txtTransferencia.setText("0");
             transferencia=0;
         }
         
        
        if(!"".equals(txtEfectivo.getText())){
            efectivo = Double.parseDouble(txtEfectivo.getText());
        }else{
            txtEfectivo.setText("0");
            efectivo=0;
        }
        
        
        
        if(verifSaldo()){
            JOptionPane.showMessageDialog(null, "ERROR: EL PAGO POR TRANSFERENCIA O EFECTIVO NO DEBE SUPERAR El TOTAL A PAGAR", "IMAGEN", JOptionPane.INFORMATION_MESSAGE);
            transferencia=0;
            efectivo=0;
            txtTransferencia.setText("0");
            txtEfectivo.setText("0");
            lblSaldo.setText("");
        }else{
            saldo = Totalpagar - efectivo - transferencia;
            lblSaldo.setText(""+saldo);
        }
    }
        
