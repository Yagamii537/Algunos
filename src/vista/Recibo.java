    private void guardar() {

        try {

            Date fechaVenta = new Date();
            String fechaActual = new SimpleDateFormat("yyyy-MM-dd").format(fechaVenta);

            String nombre, contacto, trabajador;
            int estado = 1;

            nombre = txtCliente.getText();
            contacto = txtContacto.getText();
            trabajador = txtTrabajador.getText();

            efectivo = Double.parseDouble(txtEfectivo.getText());
            transferencia = Double.parseDouble(txtTransferencia.getText());
            saldo = Totalpagar - efectivo - transferencia;

            String sql = "insert into recibo(fecha,nombreCli,contactoCli,trabajador,efectivo,transferencia,saldo,total,estado) values('" + fechaActual + "','" + nombre + "','" + contacto + "','" + trabajador + "','" + efectivo
                    + "','" + transferencia + "','" + saldo + "','" + Totalpagar + "','" + estado + "')";

            con = cn.getConnection();
            st = con.createStatement();
            st.executeUpdate(sql);

        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }

    }

    private void LimpiarTableVenta() {
        tmp = (DefaultTableModel) TablaDatos.getModel();
        int fila = TablaDatos.getRowCount();
        for (int i = 0; i < fila; i++) {
            tmp.removeRow(0);
        }
    }

