    private void RegistrarDetalle() {
        try {
            int idRecibo = 0;
            String sql = "SELECT MAX(idRecibo) FROM recibo";
            con = cn.getConnection();
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()) {
                idRecibo = rs.getInt(1);
            }

            for (int i = 0; i < TablaDatos.getRowCount(); i++) {

                int cant = Integer.parseInt(TablaDatos.getValueAt(i, 0).toString());
                String descripcion = TablaDatos.getValueAt(i, 1).toString();
                double precio = Double.parseDouble(TablaDatos.getValueAt(i, 2).toString());
                double total = cant * precio;

                String sql2 = "insert into detalle(idRecibo,cantidad,descripcion,valorUnitario) values('" + idRecibo + "','" + cant + "','" + descripcion + "','" + precio + "')";

                con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql2);

            }

        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

